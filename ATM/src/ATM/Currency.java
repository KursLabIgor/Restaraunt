package ATM;

public class Currency {

	String name;
	float amountCash;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public float getAmountCash() {
		return amountCash;
	}
	public void setAmountCash(float amountCash) {
		this.amountCash = amountCash;
	}
	
	
	
}
